// undefined vs not defined

// undefined :-
/*
    - undefined is a special keyword that javascript assigns to a variable  in the memory allocation phase.
    - undefined means that the variable has one space in the memory.
    - after the memory allocation phase, we have code execution phase where the variable get the value.
*/

// not defined :-
/*
    - not defined means that a variable has one space in the memory.
    - also we can say that the variable has no been created yet.
*/

